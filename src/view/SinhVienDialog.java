    public boolean validateForm() {
        if (txtMaSV.getText().isEmpty() || txtTenSV.getText().isEmpty()) {
            return false;
        }
        return true;

    }

    public SinhVien getModel() throws ParseException {
        SinhVien sv = new SinhVien();
        sv.setMaSV(txtMaSV.getText());
        sv.setTenSN(txtTenSV.getText());
        boolean gt = false;// mac dinh la Nu
        if (rdNam.isSelected()) {
            gt = true;// nam
        }
        sv.setGioiTinh(gt);
        sv.setDiaChi(txtDiaChi.getText());
        sv.setNgaySinh(date_format.parse(txtNgaySinh.getText()));
        if (strHinhAnh == null) {
            sv.setHinhAnh("NO AVATA");
        } else {
            sv.setHinhAnh(strHinhAnh);
        }
        return sv;
    }

    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            try {
                SinhVien sv = getModel();
                if (dao.add(sv) > 0) {
                    fillDataTable();

                    JOptionPane.showMessageDialog(this, "luu thanh cong");
//                    dao.writerToFile();
reset();

                }
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(this, "không đúng định dạng");
                Logger.getLogger(SinhVienDialog.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(this, "ban chua nhap ma SV va ten Sv");
        }
    }//GEN-LAST:event_btnLuuActionPerformed

