    public void fillDataTable() {
        DefaultTableModel model = (DefaultTableModel) tbSinhVien.getModel();
        model.setRowCount(0);//clear table.
        for (SinhVien s : dao.getAllSinhVien()) {
            Object rowData[] = new Object[6];
            rowData[0] = s.getMaSV();
            rowData[1] = s.getTenSN();
            rowData[2] = date_format.format(s.getNgaySinh());
            rowData[3] = s.isGioiTinh() ? "nam" : "nu";
            rowData[4] = s.getDiaChi();
            rowData[5] = s.getHinhAnh();
            model.addRow(rowData);
        }
    }

