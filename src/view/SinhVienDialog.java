    public void setModelToForm(SinhVien sv) {
        txtMaSV.setText(sv.getMaSV());
        txtTenSV.setText(sv.getTenSN());
        txtNgaySinh.setText(date_format.format(sv.getNgaySinh()));
        txtDiaChi.setText(sv.getDiaChi());
        if (sv.isGioiTinh()) {
            rdNam.isSelected();
        } else {
            rdNu.isSelected();
        }
        // load hinh anh
        if (sv.getHinhAnh().equalsIgnoreCase("NO AVATA")) {
            lblHinhAnh.setText("NO AVATA");
            lblHinhAnh.setIcon(null);
        } else {
            lblHinhAnh.setText("");
//            ImageIcon imgIcon = new ImageIcon(getClass().getResource("/IMG/" + sv.getHinhAnh()));
//            Image img = imgIcon.getImage().getScaledInstance(lblHinhAnh.getWidth(), lblHinhAnh.getHeight(), Image.SCALE_SMOOTH);
//            ImageIcon scaledIcon = new ImageIcon(img);
//
//            lblHinhAnh.setIcon(scaledIcon);
            ImageIcon imgIcon = new ImageIcon(getClass().getResource("/IMG/" + sv.getHinhAnh()));
            Image img = imgIcon.getImage();

            img.getScaledInstance(lblHinhAnh.getWidth(), lblHinhAnh.getHeight(), 0);
            lblHinhAnh.setIcon(imgIcon);
        }

    }
